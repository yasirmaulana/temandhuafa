                            Jenis wakaf
                        </button>
                    </div>
                    <div id="accordion15" class="accordion-body collapse" data-parent="#accordionExample2">
                        <div class="accordion-content">
                            Dalam kitab fikih, wakaf dibagi atas dua bentuk, yaitu: wakaf umum dan wakaf khusus. 
                            Wakaf umum berarti wakaf yang diberikan memiliki tujuan umum atau ditujukan kepada kelompok umum, seperti wakaf sekolah dan rumah sakit atau wakaf harta untuk kelompok fuqara atau ahli ilmu. 
                            Sementara wakaf khusus adalah wakaf yang dimaksudkan untuk tujuan khusus dan diperuntukkan untuk kelompok khusus juga, seperti wakaf sebuah tempat yang ditujukan khusus untuk seseorang atau orang-orang khusus. 
                        </div>
                    </div>
