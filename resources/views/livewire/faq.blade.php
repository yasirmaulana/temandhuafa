                    <div class="accordion-header">
                        <button class="btn collapsed" type="button" data-toggle="collapse" data-target="#accordion16">
                            Kedudukan wakaf
                        </button>
                    </div>
                    <div id="accordion16" class="accordion-body collapse" data-parent="#accordionExample2">
                        <div class="accordion-content">
                            Wakaf adalah salah satu dari bab-bab fikih yang syarat, ketentuan dan hukum-hukumnya dijelaskan dalam kitab-kitab fikih.
                            Dalam kitab-kitab hadis disebutkan mengenai wakaf berdampingan dengan sedekah, hibah dan yang serupa dengan itu.
                            <br /><br />
                            Dalam Al-Qur'an, tidak terdapat wakaf secara ekspilisit. Wakaf bersama dengan sedekah dan hibah dijelaskan dalam kitab-kitab ayatul ahkam yang dikategorikan sebagai سُکنیٰ (pemanfaatan suatu harta dalam waktu yang ditentukan) dan semua jenis pemberian مُنجّزه (pemberian harta kepada orang lain tanpa kompensasi). 
                            Wakaf disebut sebagai misdaq (perwujudan) dari sebagian ayat Al-Qur'an yang menyebut mengenai infak dan pemberian harta.
                            <br /><br />
                            Menurut riwayat, sedekah jariyah (wakaf) adalah suatu pemberian yang pahalanya terus mengalir meski si pemberi wakaf telah meninggal dunia.
                            Namun dalam riwayat-riwayat tersebut, jarang digunakan istilah wakaf dan lebih sering disebut sebagai sedekah. 
                            Namun dalam riwayat yang berkaitan dengan hukum-hukum wakaf menyebut istilah wakaf secara eksplisit.
                            <br /><br />
                            Wakaf adalah tema yag penuh dengan pembahasan di kalangan kaum muslimin, khususnya dikalangan fukaha. 
